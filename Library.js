function renderBooks(booksToShow) {
    booksContainer.innerHTML = "";

    if (booksToShow.length === 0) {
        booksContainer.innerHTML = '<p class="empty-message">לא נמצאו ספרים מתאימים.</p>';
        return;
    }

    booksToShow.forEach(function(book) {
        let card = document.createElement("div");
        card.className = "book-card";

        let img = document.createElement("img");
        img.src = book.imageUrl;
        img.alt = "תמונה של הספר " + book.title;

        let title = document.createElement("h3");
        title.innerText = book.title;

        let author = document.createElement("p");
        author.innerText = "מחבר: " + book.author;

        let publicationYear = document.createElement("p");
        publicationYear.innerText = "שנת הוצאה: " + book.publicationYear;

        let detailsButton = document.createElement("button");
        detailsButton.innerText = "מידע נוסף";

        detailsButton.addEventListener("click", function() {
            localStorage.setItem("selectedBookTitle", book.title);
            window.location.href = "book.html";
        });

        let updateButton = document.createElement("button");
        updateButton.innerText = "עדכון";

        updateButton.addEventListener("click", function() {
            updateBookFromUser(book.title);
        });

        let deleteButton = document.createElement("button");
        deleteButton.innerText = "מחיקה";

        deleteButton.addEventListener("click", function() {
            Library.removeBook(book.title);
            renderBooks(Library.books);
        });

        card.append(img);
        card.append(title);
        card.append(author);
        card.append(publicationYear);
        card.append(detailsButton);
        card.append(updateButton);
        card.append(deleteButton);

        booksContainer.append(card);
    });
}


function updateBookFromUser(oldTitle) {
    let book = Library.books.find(function(book) {
        return Library.normalizeText(book.title) === Library.normalizeText(oldTitle);
    });

    if (book === undefined) {
        return;
    }

    let newTitle = prompt("הכנס שם ספר חדש:", book.title);
    let newAuthor = prompt("הכנס שם מחבר חדש:", book.author);
    let newPublicationYear = prompt("הכנס שנת הוצאה חדשה:", book.publicationYear);
    let newImageUrl = prompt("הכנס קישור לתמונה:", book.imageUrl);
    let newSummary = prompt("הכנס תקציר חדש:", book.summary);
    let newNytReview = prompt("הכנס ביקורת ספרותית:", book.nytReview);

    if (
        newTitle === null ||
        newAuthor === null ||
        newPublicationYear === null ||
        newImageUrl === null ||
        newSummary === null ||
        newNytReview === null
    ) {
        return;
    }

    Library.updateBook(
        oldTitle,
        newTitle,
        newAuthor,
        newPublicationYear,
        newImageUrl,
        newSummary,
        newNytReview
    );

    renderBooks(Library.books);
}


